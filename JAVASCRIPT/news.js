function Added1(){
  const buttonElement = document.querySelector('.js-add-btn-1');
  
  if (buttonElement.innerHTML === "Add to playlist"){
      buttonElement.innerHTML = "Added"
  }else{
      buttonElement.innerHTML = "Add to playlist"
  }
}

